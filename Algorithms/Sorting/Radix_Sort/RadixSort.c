#include<stdio.h>
int main()
{
int numberArray[10],lowerBound,upperBound,y,i,j,e,f,Queue[10][10],front[10],rear[10],digit,largestNumber,digitCount,num;
lowerBound=0;
upperBound=9;
y=lowerBound;
while(y<=upperBound)
{
front[y]=0;
rear[y]=0;
y++;
}
y=lowerBound;
while(y<=upperBound)
{
printf("Enter the number :");
scanf("%d",&numberArray[y]);
fflush(stdin);
if(numberArray[y]<0)
{
printf("INVALID NUMBER\n");
return 0;
}
y++;
}	
largestNumber=numberArray[lowerBound];	
y=lowerBound+1;
while(y<=upperBound)
{
if(largestNumber<numberArray[y])largestNumber=numberArray[y];
y++;
}
num=largestNumber;
digitCount=1;
while(num>9)
{
digitCount++;
num=num/10;
}
e=10;
f=1;
y=1;
while(y<=digitCount)
{
i=lowerBound;
while(i<=upperBound)
{
num=numberArray[i];
digit=(num%e)/f;
Queue[digit][rear[digit]]=num;
rear[digit]++;
i++;
}
j=lowerBound;
i=lowerBound;
while(i<=upperBound)
{
while(rear[i]!=0)
{
num=Queue[i][front[i]];
front[i]++;
numberArray[j]=num;
j++;
if(rear[i]==front[i])
{
rear[i]=0;
front[i]=0;
}
}
i++;
}
e=e*10;
f=f*10;
y++;
}
y=lowerBound;
while(y<=upperBound)
{
printf("%d\n",numberArray[y]);
y++;
}
return 0;
}
